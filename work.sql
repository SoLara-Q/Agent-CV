CREATE DATABASE IF NOT EXISTS ai_interview DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci;
USE ai_interview;

SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS = 0;

DROP TABLE IF EXISTS practice_record;
DROP TABLE IF EXISTS resume_info;
DROP TABLE IF EXISTS question_bank;
DROP TABLE IF EXISTS interview_question;
DROP TABLE IF EXISTS sys_user;

-- 1. 系统用户表
CREATE TABLE sys_user (
    id BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '主键ID',
    username VARCHAR(50) NOT NULL UNIQUE COMMENT '用户名',
    password VARCHAR(255) NOT NULL COMMENT '密码，BCrypt加密',
    nickname VARCHAR(50) COMMENT '昵称',
    role VARCHAR(20) NOT NULL COMMENT '角色 ADMIN/USER',
    enabled TINYINT DEFAULT 1 COMMENT '是否启用：1启用，0禁用',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    INDEX idx_username (username),
    INDEX idx_role (role)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='系统用户表';

-- 2. 面试题库表
CREATE TABLE question_bank (
    id BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '主键ID',
    title VARCHAR(255) NOT NULL COMMENT '题目标题',
    answer TEXT COMMENT '参考答案',
    category VARCHAR(50) COMMENT '题目分类：Java基础/Spring Boot/MySQL/Redis/软件测试等',
    difficulty VARCHAR(20) COMMENT '难度：简单/中等/困难',
    job_type VARCHAR(50) COMMENT '岗位方向：Java后端/软件测试/前端开发/AI应用/通用',
    tags VARCHAR(255) COMMENT '标签，逗号分隔',
    view_count INT DEFAULT 0 COMMENT '浏览次数',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    INDEX idx_category (category),
    INDEX idx_job_type (job_type),
    INDEX idx_difficulty (difficulty),
    INDEX idx_view_count (view_count),
    FULLTEXT INDEX ft_question_search (title, answer, tags)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='面试题库表';

-- 3. 简历信息表
CREATE TABLE resume_info (
    id BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '主键ID',
    user_id BIGINT NOT NULL COMMENT '用户ID',
    real_name VARCHAR(50) NOT NULL COMMENT '真实姓名',
    target_job VARCHAR(80) NOT NULL COMMENT '目标岗位',
    skills TEXT COMMENT '技能描述',
    project_experience TEXT COMMENT '项目经历',
    ai_suggest TEXT COMMENT 'AI优化建议',
    file_path VARCHAR(255) COMMENT '上传文件路径',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    INDEX idx_user_id (user_id),
    INDEX idx_target_job (target_job),
    CONSTRAINT fk_resume_user FOREIGN KEY (user_id) REFERENCES sys_user(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='简历信息表';

-- 4. 刷题记录表，可用于后续扩展错题本/练习记录
CREATE TABLE practice_record (
    id BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '主键ID',
    user_id BIGINT NOT NULL COMMENT '用户ID',
    question_id BIGINT NOT NULL COMMENT '题目ID，对应 question_bank.id',
    result VARCHAR(20) COMMENT '练习结果，例如：掌握/未掌握/收藏/错题',
    note VARCHAR(500) COMMENT '备注',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    INDEX idx_user_id (user_id),
    INDEX idx_question_id (question_id),
    CONSTRAINT fk_practice_user FOREIGN KEY (user_id) REFERENCES sys_user(id) ON DELETE CASCADE,
    CONSTRAINT fk_practice_question FOREIGN KEY (question_id) REFERENCES question_bank(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='刷题记录表';

-- 5. 旧版题库表，保留是为了兼容旧实体 InterviewQuestion，如项目不用可不操作
CREATE TABLE interview_question (
    id BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '主键ID',
    job_type VARCHAR(50) COMMENT '岗位类型',
    question_title VARCHAR(255) COMMENT '题目标题',
    answer_analysis TEXT COMMENT '答案解析',
    difficulty VARCHAR(20) COMMENT '难度',
    hot INT DEFAULT 0 COMMENT '热度',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='旧版面试题表';

